package com.example.lista_personalizada_noche;

public class Persona {
    String image;
    String firstName;
    String familyName;
    String email;

    public Persona(String image, String firstName, String familyName, String email) {
        this.image = image;
        this.firstName = firstName;
        this.familyName = familyName;
        this.email = email;
    }
}
